%% 1. USER PARAMETERS

model = 'DataCenter_D2C_Rack_v1';

% Simulation
simulation_time_s = 3600;
load_step_time_s  = 1800;
initial_load_fraction = 0.50;
final_load_fraction   = 1.00;

% Rack
rack_U = 48;
kW_per_U = 1.0;
heat_capture_fraction = 1.00;
coldplate_paths = 48;              % One equivalent path per U
Rth_chip_to_coolant_K_W = 0.020;   % USER INPUT: K/W per cooling path

% External PG25 fluid, constant properties requested by user
nu_external_cSt = 1.95;
rho_external_kg_m3 = 1000;
cp_external_J_kgK = 3980;          % 3.98 J/g/K
k_external_W_mK = 0.49;
mu_external_Pas = nu_external_cSt * 1e-6 * rho_external_kg_m3;

% Internal cold-plate fluid.
% Placeholder: same properties as external PG25 until another fluid is set.
nu_internal_cSt = nu_external_cSt;
rho_internal_kg_m3 = rho_external_kg_m3;
cp_internal_J_kgK = cp_external_J_kgK;
k_internal_W_mK = k_external_W_mK;
mu_internal_Pas = nu_internal_cSt * 1e-6 * rho_internal_kg_m3;

% Thermal targets
deltaT_internal_K = 6;
deltaT_external_K = 5;
T_external_supply_C = 25;
HX_approach_K = 3;                  % Internal supply minus external supply

% Internal loop hydraulic model
dp_internal_ref_Pa = 120e3;
eta_internal_pump = 0.65;

% External PG25 loop hydraulic model
dp_external_ref_Pa = 80e3;
eta_external_pump = 0.70;

% Reference fluid values used in pressure-loss scaling
mu_internal_ref_Pas = mu_internal_Pas;
rho_internal_ref_kg_m3 = rho_internal_kg_m3;
mu_external_ref_Pas = mu_external_Pas;
rho_external_ref_kg_m3 = rho_external_kg_m3;
mu_pressure_exponent = 0.20;

% Closed-circuit cooling tower / heat-rejection unit
tower_design_heat_kW = 60;
tower_fan_design_kW = 1.50;
tower_spray_pump_design_kW = 0.50;

% Other rack/facility auxiliary power
auxiliary_power_kW = 0.50;

% Derived design values
rack_design_power_kW = rack_U * kW_per_U;
Q_design_W = rack_design_power_kW * heat_capture_fraction * 1000;

mdot_internal_ref_kg_s = Q_design_W / ...
    (cp_internal_J_kgK * deltaT_internal_K);
Vdot_internal_ref_m3s = mdot_internal_ref_kg_s / rho_internal_kg_m3;

% Include internal pump design estimate in CDU reference heat
P_internal_pump_design_kW = ...
    dp_internal_ref_Pa * Vdot_internal_ref_m3s / ...
    (eta_internal_pump * 1000);

Q_external_design_W = ...
    (rack_design_power_kW * heat_capture_fraction + ...
    P_internal_pump_design_kW) * 1000;

mdot_external_ref_kg_s = Q_external_design_W / ...
    (cp_external_J_kgK * deltaT_external_K);
Vdot_external_ref_m3s = mdot_external_ref_kg_s / rho_external_kg_m3;

fprintf('PG25 dynamic viscosity used: %.6f Pa.s\n', mu_external_Pas);
fprintf('Rack design IT load: %.1f kW\n', rack_design_power_kW);
fprintf('Reference internal flow: %.2f m^3/h\n', ...
    Vdot_internal_ref_m3s * 3600);
fprintf('Reference external flow: %.2f m^3/h\n', ...
    Vdot_external_ref_m3s * 3600);

