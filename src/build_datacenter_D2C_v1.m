%% build_datacenter_D2C_v1.m
% Reduced-order direct-to-chip data-center rack model.
%
% Architecture:
%   48U IT rack -> internal cold-plate loop -> rack CDU/HX
%   -> external PG25 loop -> closed-circuit heat-rejection unit
%   -> facility electrical power and energy accounting
%
% The script generates a NEW model. It does not overwrite the previous v0
% model. Edit the parameters in Section 1, then run this script again.
%
% Unit convention:
%   Power in the signal diagram: kW
%   Heat flow in hydraulic/thermal equations: W
%   Pressure: Pa
%   Flow: m^3/s internally, with m^3/h outputs
%   Temperature: degC
%
% Broad-scope assumptions:
%   - Steady-state flow required from Q = m_dot*cp*DeltaT
%   - Pressure losses scale approximately with flow^2
%   - Cold-plate thermal resistance is the TOTAL chip-to-bulk-coolant
%     resistance for one cooling path
%   - External supply temperature is imposed
%   - Heat-rejection power is a reduced-order load-based model
%
% Later versions can replace each reduced-order section with Simscape
% Fluids components without changing the top-level energy architecture.

clearvars;
